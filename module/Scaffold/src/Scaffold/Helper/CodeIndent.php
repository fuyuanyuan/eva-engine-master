namespace Scaffold\Helper;

use Eva\Api;

/**
 * View helper for rendering Form objects
 * 
 * @category   Zend
 * @package    Zend_Form
 * @subpackage View
 * @copyright  Copyright (c) 2005-2012 Zend Technologies USA Inc. (http://www.zend.com)
 * @license    http://framework.zend.com/license/new-bsd     New BSD License
 */
class CodeIndent extends \Zend\Form\View\Helper\AbstractHelper
{
    /**
     * Invoke helper as functor
     *
     * Proxies to {@link render()}.
     * 
     * @param  ElementInterface $element 
     * @return string
     */
    public function __invoke($code, $indent = 4, $indentFirstLine = false)
    {
        $code = explode("\n", $code);
        foreach($code as $i => $line){
            if($i === 0 && $indentFirstLine === false){
                continue;
            }
            $code[$i] = str_repeat(" ", $indent) . $line;
        }
        $code = implode("\n", $code);
        return $code;
    }
}
